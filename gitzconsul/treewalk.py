from pathlib import Path


def walk(root):
    """Walk down tree starting at root and return a generator among all json files"""
    for path in Path(root).iterdir():
        if path.is_dir():
            yield from walk(path)
        elif not path.is_file():
            continue
        elif path.suffix not in {'.json'}:
            continue
        else:
            yield path
